-- ---------------------------------------------------------------------
-- A função para determinar a data inicial de um período em função da
-- data instalação do equipamento e a data de início do período.
-- ---------------------------------------------------------------------
CREATE OR REPLACE FUNCTION getStartDate(FinstallDate date, FstartPeriod date)
RETURNS date AS
$BODY$
  SELECT CASE
           WHEN FinstallDate < FstartPeriod THEN FstartPeriod
           ELSE FinstallDate
         END;
$BODY$
LANGUAGE 'sql' IMMUTABLE;

-- ---------------------------------------------------------------------
-- A função para determinar a data final de um período em função da data
-- de desinstalação do equipamento (se ocorreu) e a data inicial deste
-- período.
-- ---------------------------------------------------------------------
CREATE OR REPLACE FUNCTION getEndDate(FuninstallDate date, FendPeriod date)
RETURNS date AS
$BODY$
  SELECT CASE
           WHEN FuninstallDate IS NULL THEN FendPeriod
           WHEN FuninstallDate > FendPeriod THEN FendPeriod
           ELSE FuninstallDate
         END;
$BODY$
LANGUAGE 'sql' IMMUTABLE;

